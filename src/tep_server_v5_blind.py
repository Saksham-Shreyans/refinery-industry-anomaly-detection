# ── MODEL DEFINITION ──────────────────────────────────────────────────────────
class FaultClassifierLSTM(nn.Module):
    """
    Identical to pipeline_v5.  Must stay in sync.
    Two-layer LSTM → Linear(hidden,64) → ReLU → Dropout → Linear(64,21)
    """
    def __init__(self, n_sensors, hidden=128, n_classes=NUM_CLASSES):
        super().__init__()
        self.lstm = nn.LSTM(
            input_size=n_sensors, hidden_size=hidden,
            num_layers=2, batch_first=True, dropout=0.3
        )
        self.head = nn.Sequential(
            nn.Linear(hidden, 64),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(64, n_classes)
        )

    def forward(self, x):
        out, _ = self.lstm(x)
        return self.head(out[:, -1, :])   # (B, 21) raw logits

