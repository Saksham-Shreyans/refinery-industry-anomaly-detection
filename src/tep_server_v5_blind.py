@app.route("/api/blind/score", methods=["POST"])
def api_blind_score():
    """
    Validates a batch of AI guesses against the answer key.
    POST body: { "guesses": { "1": 5, "2": 12, ... } }
    """
    if not BLIND_KEY:
        return jsonify({"error": "Answer key not loaded"}), 404

    guesses = request.get_json(force=True).get("guesses", {})
    results, correct = [], 0
    for run_str, guessed in guesses.items():
        entry = BLIND_KEY.get(run_str)
        if not entry:
            continue
        true_fault = int(entry["true_fault"])
        ok = (int(guessed) == true_fault)
        if ok:
            correct += 1
        results.append({
            "run": int(run_str), "guessed_fault": int(guessed),
            "true_fault": true_fault, "correct": ok,
        })
    total = len(results)
    return jsonify({
        "results":  results,
        "correct":  correct,
        "total":    total,
        "accuracy": round(correct / total * 100, 1) if total else 0.0,
    })

@app.route("/api/predict", methods=["POST"])
def api_predict():
    """
    Single-window inference.
    POST body: { "window": [[52 floats] × 50] }
    Returns  : { "predicted_class": int,
                 "probabilities": [21 floats],
                 "p_fault": float,          ← SPC signal value
                 "alarm": bool }            ← raw (no SPC; caller must debounce
    """
    body   = request.get_json(force=True)
    window = np.array(body["window"], dtype=np.float32)
    window = (window - MEANS) / STDS
    if len(window) < SEQUENCE_LEN:
        pad    = np.zeros((SEQUENCE_LEN - len(window), N_SENSORS), dtype=np.float32)
        window = np.vstack([pad, window])
    elif len(window) > SEQUENCE_LEN:
        window = window[-SEQUENCE_LEN:]
    X = torch.tensor(window).unsqueeze(0)
    with torch.no_grad():
        logits = MODEL(X)                           # (1, 21)
        probs  = torch.softmax(logits, dim=-1)[0]   # (21,)
        pred   = int(logits.argmax(dim=1).item())
    p_fault_val = float(1.0 - probs[0].item())
    return jsonify({
        "predicted_class": pred,
        "probabilities":   probs.tolist(),
        "p_fault":         p_fault_val,
        "alarm":           pred != 0,               # raw; no SPC applied here
        "fault_type":      FAULT_TYPES.get(pred, "unknown"),
    })

